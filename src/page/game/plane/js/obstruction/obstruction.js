const size = 50;
const body = document.body;
export const obstructArr = new Array();

export function initObstruction() {
    for (let i = 0; i < mapArr.length; i++) {
        for (let j = 0; j < mapArr[0].length; j++) {
            if (mapArr[i][j] == 1) {
                let div = document.createElement('div');
                div.className = 'obstruct';
                body.appendChild(div);
                div.style.top = i * 50 + 'px';
                div.style.left = j * 50 + 'px'
                obstructArr.push(div);
            }
        }
    }
}

export function clearObstruction(){
    for (let i = 0; i < obstructArr.length; i++) {
        document.body.removeChild(obstructArr[i]);
    }
    obstructArr.length = 0;
}