const size = 25;
const body = document.body;
const obstructArr = new Array();
export let obstructs = [];

export function initObstruction() {
    for (let i = 0; i < mapArr.length; i++) {
        for (let j = 0; j < mapArr[0].length; j++) {
            if (mapArr[i][j] == 1) {
                let div = `
                    <div class="obstruct" style="top:${i * size}px;left:${j * size}px"></div>
                `;
                obstructArr.push(div);
            }
        }
    }
    body.insertAdjacentHTML('beforeend', obstructArr.join(''));
    obstructs = body.querySelectorAll('.obstruct');
}

export function clearObstruction() {
    for (let i = 0; i < obstructs.length; i++) {
        document.body.removeChild(obstructs[i]);
    }
    obstructs = null;
    obstructArr.length = 0;
}
